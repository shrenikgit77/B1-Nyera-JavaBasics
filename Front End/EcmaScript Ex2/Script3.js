
let addition = (a, b) =>
{
    return a + b;
}

let subtraction = (a, b) =>
{
    return a - b;
}

let multiplication = (a, b) =>
{
    return a * b;
}

let division = (a, b) =>
{
    return a / b;
}


var sum = addition(10, 4);
var subt = subtraction(10, 4);
var mult = multiplication(10, 2);
var div = division(10, 3);


console.log("Addition = "  + sum);
console.log("Subtraction = " + subt);
console.log("Multiplication = " + mult);
console.log("Division = " + div);