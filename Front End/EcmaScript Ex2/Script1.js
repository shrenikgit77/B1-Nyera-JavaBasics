
var sum = addition(10, 4);
var subt = subtraction(10, 4);
var mult = multiplication(10, 2);
var div = division(10, 3);


function addition(a, b)
{
    return a + b;
}

function subtraction(a, b)
{
    return a - b;
}

function multiplication(a, b)
{
    return a * b;
}

function division(a, b)
{
    return a / b;
}


